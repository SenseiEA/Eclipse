    private void jButtonAddOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddOrderActionPerformed
        String d = "";
        String data = "";
        String costs = "";
        
        for(int i = 0; i < jTable1.getRowCount(); i++){
            name = (String) jTable1.getValueAt(i, 0);
            qty = Integer.parseInt(jTable1.getValueAt(i, 1).toString()); 
            value = Double.parseDouble(jTable1.getValueAt(i, 2).toString());
    
            
            
            data = "x" + qty + " " + name + " " + "Php "+ value;

            d += " " + data + "\n";

      }
        
        double subTotal = Double.parseDouble(jTextFieldSubTotal.getText());
        double cash = Double.parseDouble(jTextFieldCash.getText());
        double discount = Double.parseDouble(jTextFieldDiscount.getText());
        double change = Double.parseDouble(jTextFieldChange.getText());
            
        costs = "\nCost: " + subTotal + "\n"
                    + "Cash Paid: " + cash + "\n"
                    + "Discount: " + discount + "\n" 
                    + "Checkout: " + change;
        
        if("".equals(order.getGUI().getText())){
                order.getGUI().setText(d + costs);
            } else if ("".equals(order.getGUI2().getText())){
                order.getGUI2().setText(order.getGUI().getText());
                order.getGUI().setText(d + costs);
                
            } else if ("".equals(order.getGUI3().getText())){
                order.getGUI3().setText(order.getGUI2().getText());
                order.getGUI2().setText(order.getGUI().getText());
                order.getGUI().setText(d + costs);
                
            } else if ("".equals(order.getGUI4().getText())){
                order.getGUI4().setText(order.getGUI2().getText());
                order.getGUI3().setText(order.getGUI2().getText());
                order.getGUI2().setText(order.getGUI().getText());
                order.getGUI().setText(d + costs);
            } else {
                order.getGUI4().setText(order.getGUI3().getText());
                order.getGUI3().setText(order.getGUI2().getText());
                order.getGUI2().setText(order.getGUI().getText());
                order.getGUI().setText(d + costs);
            }
        
        JOptionPane.showMessageDialog(null, "The current order has been saved in the History tab!", "Order Added", 1);
        
    }//GEN-LAST:event_jButtonAddOrderActionPerformed

