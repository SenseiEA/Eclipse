   public void ItemCost(){
       double sum = 0;
       
       for(int i = 0; i < jTable1.getRowCount(); i++){
       
           sum += Double.parseDouble(jTable1.getValueAt(i, 2).toString());
           
           jTextFieldSubTotal.setText(Double.toString(sum));
           double cTotal = Double.parseDouble(jTextFieldSubTotal.getText());
           
           String iSubTotal = String.format("%.2f", cTotal);
           jTextFieldSubTotal.setText(iSubTotal);
           
       } 
   } 
    
    
   public void Change(){
       double sum = 0;
       double cash = Double.parseDouble(jTextFieldCash.getText());
       
       for(int i = 0; i <jTable1.getRowCount(); i++){
           
           sum += Double.parseDouble(jTable1.getValueAt(i, 2).toString());
           
           double cChange = cash - sum;
           
           
           String ChangeGiven = String.format("%.2f", cChange);
           jTextFieldChange.setText(ChangeGiven);
           
       }
   }
   
   public void Discount(){
       
           double subTotal = Double.parseDouble(jTextFieldSubTotal.getText());
           double cash = Double.parseDouble(jTextFieldCash.getText());
           
           double cDiscount =  (subTotal * 0.2);
           double FinalDiscount = subTotal - cDiscount;
           
           String Discount = String.format("%.2f", cDiscount);
           jTextFieldDiscount.setText(Discount);
           
           String DiscountGiven = String.format("%.2f", cash - FinalDiscount);
           jTextFieldChange.setText(DiscountGiven);
   }
   
